/*
Math

- Um objeto com propriedades e métodos matemáticos
- Podemos saber o valor de PI e até gerar números aleatórios
*/

console.log(Math.PI) // 3.141592653589793
console.log(Math.LN2) // 0.6931471805599453
console.log(Math.LN10) // 2.302585092994046
console.log(Math.floor(Math.random() * 11)) // floor arrendonda numeros para baixo

/*

- Temos também métodos de arredondamento como: floor, ceil e round;
- Métodos para calcular potência;
- E também raiz quadrada;

*/

console.log(Math.floor(123.55)); // 123 Arredonda para baixo
console.log(Math.ceil(123.55)) // 124 Arredonda para cima
console.log(Math.pow(12,2)); // 144 Potencia
console.log(Math.sqrt(9)); // 3 Raiz quadrada